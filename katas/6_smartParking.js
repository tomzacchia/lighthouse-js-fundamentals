function whereCanIPark(spots, vehicle) {
  for (var row = 0; row < spots.length; row++) {
    for (var col = 0; col < spots.length; col++) {
      var isRegularValid = vehicle === "regular" && /[R]/.test(spots[row][col]);
      var isSmallValid = vehicle === "small" && /[RS]/.test(spots[row][col]);
      var isMotorcycleValid =
        vehicle === "motorcycle" && /[RSM]/.test(spots[row][col]);

      if (isRegularValid || isSmallValid || isMotorcycleValid)
        return [col, row];
    }
  }

  return false;
}

console.log(
  whereCanIPark(
    [
      // COLUMNS ARE X
      // 0    1    2    3    4    5
      ["s", "s", "s", "S", "R", "M"], // 0 ROWS ARE Y
      ["s", "M", "s", "S", "r", "M"], // 1
      ["s", "M", "s", "S", "r", "m"], // 2
      ["S", "r", "s", "m", "r", "M"], // 3
      ["S", "r", "s", "m", "r", "M"], // 4
      ["S", "r", "S", "M", "M", "S"], // 5
    ],
    "regular"
  )
);

